package model;

public class YearAndDayOfYear {

	private int year;
	private int day;

	public YearAndDayOfYear() {
	}

	public YearAndDayOfYear(int year, int day) {
		this.year = year;
		this.day = day;
	}

	/**
	 * @return the year
	 */
	public int getYear() {
		return year;
	}

	/**
	 * @param year
	 *            the year to set
	 */
	public void setYear(int year) {
		this.year = year;
	}

	/**
	 * @return the day
	 */
	public int getDay() {
		return day;
	}

	/**
	 * @param day
	 *            the day to set
	 */
	public void setDay(int day) {
		this.day = day;
	}

}
